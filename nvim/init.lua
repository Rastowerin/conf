local function set_dap_colors()
	local red = "#f7768e"
	local yellow = "#e0af68"
	local blue = "#7aa2f7"
	local cyan = "#7dcfff"
	local bg2 = "#1f2335"
	vim.api.nvim_set_hl(0, "DapBreakpoint", { fg = red })
	vim.api.nvim_set_hl(0, "DapBreakpointLine", { bg = bg2 })
	vim.api.nvim_set_hl(0, "DapBreakpointCondition", { fg = yellow })
	vim.api.nvim_set_hl(0, "DapBreakpointRejected", { fg = red })
	vim.api.nvim_set_hl(0, "DapLogPoint", { fg = blue })
	vim.api.nvim_set_hl(0, "DapStopped", { fg = cyan })
	vim.api.nvim_set_hl(0, "DapStoppedLine", { bg = "#2a3158" })
end
